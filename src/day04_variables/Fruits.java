package day04_variables;

public class Fruits {
    public static void main(String[] args) {

        int apples = 70;
        int grapes = 80;
        int bananas = 100;

        System.out.println("This is how many apples I have: " + 60);
        System.out.println("This is how many apples I have: " + apples);

        System.out.println(grapes);                // grapes.sout
        System.out.println("Grapes = " + grapes);  // grapes.soutv

        System.out.println("Bananas = " + bananas);

        System.out.println("--------------------------");

        System.out.println("I sold 30 apples");
        apples = 40;
        System.out.println("This is how many apples I have after the sale: " + apples);

        System.out.println("--------------------------");

        double price = 15.99;
        System.out.println("The discount price for all the left over apples is $" + "15.99");
        System.out.println("The discount price for all the left over apples is $" + price);

        System.out.println("--------------------------");

        System.out.println(50 + 50);  // plus (math)
        System.out.println("50" + 50);// CONCATINATION

        System.out.println(10 + 10 + "10" + 10 + 10);
        System.out.println("10" + 10 + "10" + 10 + 10);

    }

}
